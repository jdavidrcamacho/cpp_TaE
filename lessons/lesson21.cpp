// 21 - Assignment and Increment Operators
#include <iostream>  

using namespace std;

//main function
int main()
{

float x = 10;

x += 5; //adds 5 to x
cout << x << "\n";

x -= 4; //subtracks 4 to x
cout << x << "\n";

x *= 3; //multiplies x by 3
cout << x << "\n";

x /= 7; //divides x by 7
cout << x << "\n";

x++; // adds 1 to x
cout << x << "\n";

x--; // subtracts 1 to x
cout << x << "\n";

}
//end of main function

//to run in terminal do
// $ g++ lesson21.cpp -o main
// $ ./main
